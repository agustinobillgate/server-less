	kontakt_nr = sa.Column(sa.Integer, default=0)
	kontignr = sa.Column(sa.Integer, default=0)
	l_zuordnung = sa.Column(ARRAY(sa.Integer),default=[0,0,0,0,0])
	memodatum = sa.Column(sa.Date, default=lambda: get_current_date())
	memousercode = sa.Column(sa.String, default="")
	memozeit = sa.Column(sa.Integer, default=0)
	memozinr = sa.Column(sa.String, default="")
