	old_zinr = sa.Column(sa.String, default="")
	pin_code = sa.Column(sa.String, default="")
	prov_till = sa.Column(sa.Date, default=None)
	pseudofix = sa.Column(sa.Boolean, default=False)
	reserve_char = sa.Column(sa.String, default="")
	reserve_dec = sa.Column(sa.Numeric, default=0)
	reserve_int = sa.Column(sa.Integer, default=0)
	resflag = sa.Column(sa.Integer, default=0)
	reslinnr = sa.Column(sa.Integer, default=1)
	resname = sa.Column(sa.String, default="")
	resnext = sa.Column(sa.Integer, default=0)
	resnr = sa.Column(sa.Integer, default=0)
	resprev = sa.Column(sa.Integer, default=0)
	resstatus = sa.Column(sa.Integer, default=1)
	setup = sa.Column(sa.Integer, default=0)
